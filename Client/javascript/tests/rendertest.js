tiles = [];

assets = 0;

function assetCheck() {
    assets++;
    if (assets == images.length)
    {
        console.log(assets, " assets have been loaded!");
        doThings()
    }
}

for (var i = 0; i < images.length; i++)
{
    var img = new Image();
    img.src = imagedir + "/" + images[i];
    img.onload = assetCheck;
    tiles.push(img);
}

function doThings() {
    context.clearRect(0, 0, 640, 480);
    context.imageSmoothingEnabled = false;

    for (i = 0; i < tileMap.length; i++)
    {
        currentMap = tileMap[i];
        for (var j = 0; j < currentMap.length; j++)
        {
            var x = 320 - 16 + (j * 16) - (i * 16);
            var y = 400 - ((currentMap.length * 16) / 2) - 8 + (i * 8) + (j * 8);
            var tile = tiles[currentMap[j]];
            context.drawImage(tile, x, y, 32, 16)
        }
    }
    context.fillStyle = "#FF0000";
    context.fillRect(300, 50, 2, 2);

    requestAnimationFrame(doThings);
}